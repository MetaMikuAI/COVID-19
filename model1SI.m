t = 0:0.1:128; %时间从0(天)到128(天)
N = 10000; %地区总人数10000(人)
I_0 = 2; %初(t=0)病例2(人)
r = 5;  %每人每天接触人数 5
p = 0.03; %接触后传染概率 0.03
I = N*I_0 ./ (I_0+(N-I_0)*exp(-r*p*t)); %感染者
S = N-I; %易感者
plot(t,I,'r'); %绘制图像：I-t
hold on
plot(t,S,'b'); %绘制图像：S-t
legend('感染者I','易感者S');
xlabel('时间t(天)'); %添加横轴标签
ylabel('人数(人)'); %添加纵轴标签
title(['模型一(SI)：N=',num2str(N),',I(0)=',num2str(I_0),',r=',num2str(r),',p=',num2str(p)]);